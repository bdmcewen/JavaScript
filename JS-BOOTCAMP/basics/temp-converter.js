// Challenge app to convert a temperature value  Celsius and Kelvin. 

let temp_f = 50 // 0c - 273.15k

// convert to Celsius
let temp_c = (temp_f-32) * 5 / 9

// convert to Kelvin
let temp_k = (temp_f + 459.67) * 5 / 9 

console.log("Fahrenheit = " + temp_f)
console.log("Celsius = " + temp_c)
console.log("Kelvin = " + temp_k)





